    /**
     * 字符常量：空字符串 {@code ""}
     */
    String EMPTY = "";

    /**
     * 字符常量：空对象 {@code "{}"}
     */
    String EMPTY_JSON = "{}";

    /**
     * 字符常量：空格符 {@code " "}
     */
    String SPACE = " ";

    /**
     * 字符常量：制表符 {@code "\t"}
     */
    String TAB = "\t";

    /**
     * 字符常量：点  {@code "."}
     */
    String POINT = ".";

    /**
     * 字符常量：分号 {@code ";"}
     */
    String SEMICOLON = ";";

    /**
     * 字符常量：逗号 {@code ","}
     */
    String COMMA = ",";

    /**
     * 字符常量：全局通用分隔符 {@code ","}
     */
    String DELIMITER = COMMA;

    /**
     * 字符常量：全局通用分隔符 下划线 {@code "_"}
     */
    String UNDERLINE = "_";

    /**
     * 字符常量：全局通用 横杠 {@code "-"}
     */
    String HORIZONTAL = "-";

